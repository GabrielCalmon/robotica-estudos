#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << 10 % 3 << endl;     // resto da divisão interia

    float myNum = 5.5;
    myNum++;                    //myNum = myNum + 1;
    cout << myNum << endl;

    // o ponto decimal faz difereança na divisão
    cout << "Divisão entre inteiros: " << 10/3 << endl;
    cout << "Divisão entre decimais: " << 10.0/3.0 << endl;

    cout << pow(2,5) << endl;

    return 0;
}